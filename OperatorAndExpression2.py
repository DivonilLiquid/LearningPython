#Comparison Operator     ==  !=  <  >  <=  >=
print("Comparison Operator")
print(124==132)
print(ord("A"))                   #ord gives ascii value of any character
print(124!=132)
print(124<132)
print(124>132)
print(124<=132)
print(124>=132)
print("abc">"ABC")
print("abd">"abc")
print("abc">"yz")
print("abc">"d")
